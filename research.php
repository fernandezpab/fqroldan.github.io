			<h2>Markups Under Uncertainty: Variations on Price Decisions in High Inflation</h2>
			<h3>with Daniel Heymann, in Macroeconomics and Development: Roberto Frenkel and the Economics of Latin America, New York: Columbia University Press. 2016</h3>

			<p>We revisit some of the contributions of Roberto Frenkel's (1979) classic paper in a modern setting. We are interested in situations in which higher uncertainty about future inflation induces firms to increase markups. It turns out that, although a simple CES model predicts the opposite, markups do increase in response to uncertainty when minimum (or maximum) capacity and liquidity constraints are considered.</p>
